#!/usr/bin/env node

/**
 * Reads openapi.json and generates types.ts barrel file
 * that re-exports all schema types from api-types.d.ts.
 *
 * Usage: node scripts/generate-types-barrel.mjs
 */

import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const CONTRACTS_DIR = join(__dirname, '..', 'src', 'contracts', 'openapi')
const OPENAPI_PATH = join(CONTRACTS_DIR, 'openapi.json')
const OUTPUT_PATH = join(CONTRACTS_DIR, 'types.ts')

// --- Manual additions (not in OpenAPI schemas) ---

const LEGACY_ALIASES = {
  MenuStoreResponse: 'CatalogStoreCatalogResponse',
  MenuCategoriesResponse: 'CatalogCategoriesResponse',
  MenuCategoryResponse: 'CatalogCategoryResponse',
  MenuItemPath: 'CatalogItemPath',
  MenuItemResponse: 'CatalogItemResponse',
  MenuItemsQuery: 'CatalogItemsQuery',
  MenuItemsResponse: 'CatalogItemsResponse',
}

const MANUAL_TYPES = ''

// --- Generate ---

const spec = JSON.parse(readFileSync(OPENAPI_PATH, 'utf-8'))
const schemaNames = Object.keys(spec.components?.schemas ?? {}).sort()

if (schemaNames.length === 0) {
  console.error('No schemas found in openapi.json')
  process.exit(1)
}

const lines = [
  '// This file is auto-generated by scripts/generate-types-barrel.mjs',
  '// Do not edit manually. Run `make sync-api` to regenerate.',
  '',
  "import type { components } from './api-types'",
  '',
]

for (const name of schemaNames) {
  lines.push(`export type ${name} = components['schemas']['${name}']`)
}

if (Object.keys(LEGACY_ALIASES).length > 0) {
  lines.push('')
  lines.push('// Legacy aliases')
  for (const [alias, target] of Object.entries(LEGACY_ALIASES)) {
    lines.push(`export type ${alias} = ${target}`)
  }
}

if (MANUAL_TYPES.trim()) {
  lines.push('')
  lines.push('// Types not in OpenAPI schemas (path/query params inlined in operations)')
  lines.push(MANUAL_TYPES)
}

lines.push('')

writeFileSync(OUTPUT_PATH, lines.join('\n'), 'utf-8')
console.log(`Generated ${OUTPUT_PATH} with ${schemaNames.length} schema types`)
